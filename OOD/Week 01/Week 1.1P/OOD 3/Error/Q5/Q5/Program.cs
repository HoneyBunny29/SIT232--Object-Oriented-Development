

/*  Q5
 * Modify the method from DEfault to default;
 * Modify the method from Case 1 to Case 1; 
 * Modify the method from Case 2 to Case 2; 
 * Variable declaration 'int n=0'. .*/


namespace SwitchStatement
{
    class SwitchStatement
    {
        static void Main(string[] args)
        {
            int n = 0;
            switch (n)
            {
                case 1: Console.WriteLine("A"); break;
                case 2: Console.WriteLine("B"); break;
                default: Console.WriteLine("C"); break;
            }
        }
    }
}


